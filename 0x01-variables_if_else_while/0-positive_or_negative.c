#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**
 *main - print result of the number either positive , negative or zero
 *
 *return: always 0.
 **/

int main(void)
{
	int n;

	srand(time(0));
	n; rand(void) - RAND_MAX / 2;

	if (n > 0)
	printf("int is positive\n,n");
	else if (n == 0)
	printf("int is zero\n,n");
	else
	printf("int is negative\n,n");
	return (0);
}
